#!/usr/bin/env python3
"""
This script defines a function for matrix transpose using NumPy.
"""


def np_transpose(matrix):
    """
    Perform matrix transpose using NumPy's transpose function.

    Parameters:
    - matrix: The matrix to be transposed as a NumPy array.

    Returns:
    - The transposed matrix as a NumPy array.
    """
    return matrix.transpose()
