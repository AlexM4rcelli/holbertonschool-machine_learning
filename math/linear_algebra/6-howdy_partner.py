#!/usr/bin/env python3
"""
This script defines a function to concatenate two arrays.
"""

def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays and returns the result.

    Parameters:
    - arr1 (list): The first array to be concatenated.
    - arr2 (list): The second array to be concatenated.

    Returns:
    - list: The concatenated array containing elements from arr1 followed by elements from arr2.
    """
    return arr1 + arr2
